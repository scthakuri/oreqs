'use client';

import { useState } from 'react';
import Link from 'next/link';
import {
    BadgeCheck,
    Bell,
    ChevronsUpDown,
    KeyRound,
    LogOut,
    User as UserIcon,
} from 'lucide-react';
import useDialogState from '@/hooks/use-dialog-state';
import { Avatar, AvatarFallback, AvatarImage } from '@/components/ui/avatar';
import {
    DropdownMenu,
    DropdownMenuContent,
    DropdownMenuGroup,
    DropdownMenuItem,
    DropdownMenuLabel,
    DropdownMenuSeparator,
    DropdownMenuTrigger,
} from '@/components/ui/dropdown-menu';
import {
    SidebarMenu,
    SidebarMenuButton,
    SidebarMenuItem,
    useSidebar,
} from '@/components/ui/sidebar';
import { SignOutDialog } from '@/components/sign-out-dialog';
import { ChangePasswordDialog } from '@/components/auth/change-password-dialog'; // Import the new dialog
import { useAuth } from '@/context/auth-provider';

export function NavUser() {
    const { isMobile } = useSidebar();
    const [openSignOutDialog, setOpenSignOutDialog] = useDialogState();
    const [showChangePasswordDialog, setShowChangePasswordDialog] = useState(false); // State for Change Password Dialog
    const { user } = useAuth();

    if (!user) return null;

    const userName = `${user.first_name} ${user.last_name}`.trim() || user.email;
    const userInitials = (user.first_name?.[0] || '') + (user.last_name?.[0] || '') || user.email?.[0]?.toUpperCase() || 'U';

    return (
        <>
            <SidebarMenu>
                <SidebarMenuItem>
                    <DropdownMenu>
                        <DropdownMenuTrigger asChild>
                            <SidebarMenuButton
                                size='lg'
                                className='data-[state=open]:bg-sidebar-accent data-[state=open]:text-sidebar-accent-foreground'
                            >
                                <Avatar className='h-8 w-8 rounded-lg'>
                                    <AvatarImage src='' alt={userName} />
                                    <AvatarFallback className='rounded-lg uppercase'>
                                        {userInitials}
                                    </AvatarFallback>
                                </Avatar>
                                <div className='grid flex-1 text-start text-sm leading-tight'>
                                    <span className='truncate font-semibold'>{userName}</span>
                                    <span className='truncate text-xs'>{user.email}</span>
                                </div>
                                <ChevronsUpDown className='ms-auto size-4' />
                            </SidebarMenuButton>
                        </DropdownMenuTrigger>
                        <DropdownMenuContent
                            className='w-(--radix-dropdown-menu-trigger-width) min-w-56 rounded-lg'
                            side={isMobile ? 'bottom' : 'right'}
                            align='end'
                            sideOffset={4}
                        >
                            <DropdownMenuLabel className='p-0 font-normal'>
                                <div className='flex items-center gap-2 px-1 py-1.5 text-start text-sm'>
                                    <Avatar className='h-8 w-8 rounded-lg'>
                                        <AvatarImage src='' alt={userName} />
                                        <AvatarFallback className='rounded-lg uppercase'>
                                            {userInitials}
                                        </AvatarFallback>
                                    </Avatar>
                                    <div className='grid flex-1 text-start text-sm leading-tight'>
                                        <span className='truncate font-semibold'>{userName}</span>
                                        <span className='truncate text-xs'>{user.email}</span>
                                    </div>
                                </div>
                            </DropdownMenuLabel>
                            <DropdownMenuSeparator />
                            <DropdownMenuGroup>
                                <DropdownMenuItem asChild>
                                    <Link href='/admin/settings/profile'>
                                        <UserIcon />
                                        Profile
                                    </Link>
                                </DropdownMenuItem>
                                <DropdownMenuItem onClick={() => setShowChangePasswordDialog(true)}> {/* Modified */}
                                    <KeyRound />
                                    Change Password
                                </DropdownMenuItem>
                                <DropdownMenuItem asChild>
                                    <Link href='/admin/settings/notifications'>
                                        <Bell />
                                        Notifications
                                    </Link>
                                </DropdownMenuItem>
                            </DropdownMenuGroup>
                            <DropdownMenuSeparator />
                            <DropdownMenuItem
                                variant='destructive'
                                onClick={() => setOpenSignOutDialog(true)}
                            >
                                <LogOut />
                                Sign out
                            </DropdownMenuItem>
                        </DropdownMenuContent>
                    </DropdownMenu>
                </SidebarMenuItem>
            </SidebarMenu>

            <SignOutDialog open={!!openSignOutDialog} onOpenChange={setOpenSignOutDialog} />
            <ChangePasswordDialog open={showChangePasswordDialog} onOpenChange={setShowChangePasswordDialog} /> {/* Render the new dialog */}
        </>
    );
}